package oct25pyramid;

public class example1 {

	public static void main(String[] args) {
		    for (int i=1 ; i<=5; i++)
		    {         // define the num of row 
		    	
		    	for(int j=1;j<=i;j++) // define the pattern 
		    	{
		    		System.out.print(i+" ");
		    	}
		    	
		    	
		    	
		    System.out.println();
		    }
		    System.out.println("*************************");
		    for (int i=5 ; i>=1; i--)
		    {         // define the num of row 
		    	
		    	for(int j=1;j<=i;j++)
		    	{
		    		System.out.print(i+" ");
		    	}
		    	  System.out.println();
		    }
		    
		    
		    System.out.println("*************************");
		  
		    for (char ch ='a' ; ch >='e'; ch++)
		    {         // define the num of row 
		    	
		    	
		    	
		    		System.out.print(ch);
		    	}
		    	
		    }
		    
		    }
		    
		    
		    
		    
		    
		    
		    
		    
		    
		     

	

