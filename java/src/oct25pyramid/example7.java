package oct25pyramid;

public class example7 {

	public static void main(String[] args) {
		    for (int i=5 ; i>=1; i--)
		    {         // define the num of row 
		    	
		    	for(int j=1;j<=i;j++) // define the pattern 
		    	{
		    		System.out.print(j+" ");
		    	}
		    	
		    	System.out.println();
		  
		    }
	}
}
		    
		    
		    
		    
		    
		    
		    
		    
		    
		    
		    
		     

	

