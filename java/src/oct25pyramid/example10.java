public class example10 {

	public static void main(String[] args) {
		  for(int i=7; i>0;i--) { // it can define no of line 
			  
			 for(int j =1;j<=i;j++) {  // it can be define no of space 
				 System.out.print(" ");
			 }
			 
			 for(int j=i; j<=7;j++) {
				 System.out.print("* ");
			 }
			 System.out.println();
		  }
		  
		  
		  
			  
			
		    }
		    }
		    
		   
		  
		    
	

		    
		    
		    
		    
		    
		    
		    
		    
		    
		    
		    
		     

	

