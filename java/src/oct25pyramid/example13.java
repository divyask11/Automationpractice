public class example13 {

	public static void main(String[] args) {
		int num;
		  for(int i=7; i<0;i--) { // it can define no of line 
			  
			 for(int j =1;j<=i;j++) {  // it can be define no of space 
				 System.out.print(" ");
			 }
			 num=1;
			 for(int j=i; j<=7;j++) {
				 System.out.print(num+ "  ");
				 num++;
			 }
			 System.out.println();
		  }
		  
		  
		  
			  
			
		    }
		    }
		    
		   
		  
		    
	

		    
		    
		    
		    
		    
		    
		    
		    
		    
		    
		    
		     

	

