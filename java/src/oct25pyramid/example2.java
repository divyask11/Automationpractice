package oct25pyramid;

public class example2 {

	public static void main(String[] args) {
		    for (int i=1 ; i<=5; i++)
		    {         // define the num of row 
		    	
		    	for(int j=1;j<=i;j++) // define the pattern 
		    	{
		    		System.out.print(j+" ");
		    	}
		    	
		    	System.out.println();
		  
		    }
	}
}
		    
		    
		    
		    
		    
		    
		    
		    
		    
		    
		    
		     

	

